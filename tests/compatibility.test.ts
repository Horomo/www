test('computeElementBalance — balanced chart (no missing, no dominant)', () => {
  // Stems: 甲(0,wood) 丙(2,fire) 戊(4,earth) 庚(6,metal) 壬(8,water) 乙(1,wood) 丁(3,fire) 己(5,earth)
  // Branches: 午(6,fire) 申(8,metal) 寅(2,wood) 子(0,water) 丑(1,earth) 酉(9,metal) 亥(11,water) 午(6,fire)
  // Combined: wood=3(甲+乙+寅), fire=3(丙+丁+午×2... wait let me recount)
  //
  // Person A pillars (no hour):
  //   year:  stem=甲(0,wood),  branch=午(6,fire)  → wood, fire
  //   month: stem=丙(2,fire),  branch=申(8,metal) → fire, metal
  //   day:   stem=壬(8,water), branch=寅(2,wood)  → water, wood
  // Person B pillars (no hour):
  //   year:  stem=乙(1,wood),  branch=丑(1,earth) → wood, earth
  //   month: stem=庚(6,metal), branch=亥(11,water)→ metal, water
  //   day:   stem=丙(2,fire),  branch=辰(4,earth) → fire, earth   ← 辰=earth idx 4
  //
  // Combined: wood=3, fire=3, earth=2, metal=2, water=2 → balanced
  const pillarsA = makePillars(
    0,  6,   // year:  甲(wood), 午(fire)
    2,  8,   // month: 丙(fire), 申(metal)
    8,  2,   // day:   壬(water), 寅(wood)
  );
  const pillarsB = makePillars(
    1,  1,   // year:  乙(wood), 丑(earth)
    6,  11,  // month: 庚(metal), 亥(water)
    2,  4,   // day:   丙(fire), 辰(earth)
  );

  const balance = computeElementBalance(pillarsA, pillarsB);
  assert.equal(balance.missing.length,  0, 'no missing elements');
  assert.equal(balance.dominant.length, 0, 'no dominant elements');
  assert.ok(balance.counts['wood']  >= 1);
  assert.ok(balance.counts['fire']  >= 1);
  assert.ok(balance.counts['earth'] >= 1);
  assert.ok(balance.counts['metal'] >= 1);
  assert.ok(balance.counts['water'] >= 1);
});

test('computeElementBalance — chart with a missing element (no metal)', () => {
  // All stems and branches chosen to exclude metal:
  //   Non-metal stems:  甲(0,wood) 乙(1,wood) 丙(2,fire) 丁(3,fire) 戊(4,earth) 己(5,earth) 壬(8,water) 癸(9,water)
  //   Non-metal branches: 子(0,water) 丑(1,earth) 寅(2,wood) 卯(3,wood) 辰(4,earth) 巳(5,fire) 午(6,fire) 未(7,earth) 戌(10,earth) 亥(11,water)
  //
  // Person A: year=甲子, month=丙寅, day=戊午  → wood,water | fire,wood | earth,fire
  // Person B: year=癸丑, month=乙亥, day=丁未  → water,earth | wood,water | fire,earth
  const pillarsA = makePillars(0, 0, 2, 2, 4, 6);   // 甲子 丙寅 戊午
  const pillarsB = makePillars(9, 1, 1, 11, 3, 7);  // 癸丑 乙亥 丁未

  const balance = computeElementBalance(pillarsA, pillarsB);
  assert.ok(balance.missing.includes('metal'), 'metal should be missing');
  assert.equal(balance.counts['metal'], 0);
});

test('computeElementBalance — chart with a dominant element (water >= 6)', () => {
  // Person A: all-water pillars (no hour)
  //   year=壬子, month=癸亥, day=壬子 → water×6
  // Person B: mixed (just needs to be valid)
  //   year=甲午, month=戊申, day=丙丑
  const pillarsA = makePillars(
    8,  0,   // year:  壬(water), 子(water)
    9,  11,  // month: 癸(water), 亥(water)
    8,  0,   // day:   壬(water), 子(water)
  );
  const pillarsB = makePillars(
    0,  6,   // year:  甲(wood), 午(fire)
    4,  8,   // month: 戊(earth), 申(metal)
    2,  1,   // day:   丙(fire), 丑(earth)
  );

  const balance = computeElementBalance(pillarsA, pillarsB);
  assert.ok(balance.counts['water'] >= 6, 'water should be dominant');
  assert.ok(balance.dominant.includes('water'), 'water should appear in dominant list');
});
