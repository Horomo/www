import test from 'node:test';
import assert from 'node:assert/strict';

import {
  getBranchInteraction,
  getDayMasterRelationship,
  computeCompatibilityTier,
  computeElementBalance,
  SIX_HARMONY,
  SIX_CLASH,
} from '../src/lib/compatibility';
import { STEMS, BRANCHES, type BaziResult } from '../src/lib/bazi';

// ── Helpers ────────────────────────────────────────────────

function makePillars(
  yearStem: number, yearBranch: number,
  monthStem: number, monthBranch: number,
  dayStem: number,   dayBranch: number,
  hourStem?: number, hourBranch?: number,
): BaziResult['pillars'] {
  const p = (s: number, b: number) => ({
    stem: STEMS[s], branch: BRANCHES[b], stemIdx: s, branchIdx: b,
  });
  return {
    year:  p(yearStem, yearBranch),
    month: p(monthStem, monthBranch),
    day:   p(dayStem, dayBranch),
    hour:  hourStem !== undefined && hourBranch !== undefined
      ? p(hourStem, hourBranch)
      : null,
  };
}

// ══════════════════════════════════════════════════════════
// 1. Six Harmony / Six Clash detection
// ══════════════════════════════════════════════════════════

test('getBranchInteraction — all 6 六合 pairs, both orderings', () => {
  for (const [x, y] of SIX_HARMONY) {
    assert.equal(getBranchInteraction(x, y), 'six_harmony', `${x}+${y} should be six_harmony`);
    assert.equal(getBranchInteraction(y, x), 'six_harmony', `${y}+${x} (reversed) should be six_harmony`);
  }
});

test('getBranchInteraction — all 6 六冲 pairs, both orderings', () => {
  for (const [x, y] of SIX_CLASH) {
    assert.equal(getBranchInteraction(x, y), 'six_clash', `${x}+${y} should be six_clash`);
    assert.equal(getBranchInteraction(y, x), 'six_clash', `${y}+${x} (reversed) should be six_clash`);
  }
});

test('getBranchInteraction — neutral pair is neither harmony nor clash', () => {
  // 子(Rat) and 寅(Tiger) are not in any six-harmony or six-clash pair
  assert.equal(getBranchInteraction('子', '寅'), 'neutral');
  assert.equal(getBranchInteraction('寅', '子'), 'neutral');
});

